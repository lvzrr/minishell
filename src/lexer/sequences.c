#include "mini_lexer.h"

bool	isvalidident(char c)
{
	return (c == '_' || c == '/'
		|| c == '.' || c == '-'
		|| c == '@' || c == '~'
		|| c == '\\' || c == '*'
		|| c == '\'' || c == '\"'
		|| ft_isalnum(c));
}

bool	isvalidop(char c)
{
	return (c == '&' || c == '|'
		|| c == '=' || c == '<'
		|| c == '>' || c == ';'
		|| c == '!' || c == '('
		|| c == '$' || c == '?'
		|| c == ')');
}
